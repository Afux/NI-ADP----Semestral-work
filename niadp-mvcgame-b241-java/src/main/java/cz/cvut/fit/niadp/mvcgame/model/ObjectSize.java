package cz.cvut.fit.niadp.mvcgame.model;

public class ObjectSize {
    int width;
    int height;
    public ObjectSize(int width, int height){
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
